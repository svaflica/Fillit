#include "libft.h"

void				ft_putstr(char const *s)
{
	unsigned char	*s1;

	if (s == NULL)
		return ;
	s1 = (unsigned char	*)s;
	while (*s1)
	{
		ft_putchar(*s1);
		s1++;
	}
}
